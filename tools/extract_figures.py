PAD = 6      # 裁切框留白(point)
DPI = 200    # 輸出解析度


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--year', type=int, required=True)
    ap.add_argument('--src', default='source-pdfs')
    ap.add_argument('--out', default='public/images')
    ap.add_argument('--json', default=None)
    args = ap.parse_args()

    year = args.year
    src_dir = Path(args.src) / str(year)
    out_dir = Path(args.out) / str(year)
    out_dir.mkdir(parents=True, exist_ok=True)
    json_path = Path(args.json or f'src/data/questions.{year}.json')

    docs = {}

    def render(pdf, page, x0, y0, x1, y1):
        if pdf not in docs:
            docs[pdf] = fitz.open(src_dir / pdf)
        p = docs[pdf][page]
        clip = fitz.Rect(x0 - PAD, y0 - PAD, x1 + PAD, y1 + PAD) & p.rect
        pix = p.get_pixmap(clip=clip, matrix=fitz.Matrix(DPI / 72, DPI / 72))
        return Image.open(io.BytesIO(pix.tobytes('png'))).convert('RGB')

    # 一題可有多個區塊(可跨頁),依序垂直拼接成一張
    grouped = OrderedDict()
    for entry in LAYOUT[year]:
        if len(entry) == 7:
            pdf, page, num, x0, y0, x1, y1 = entry
        else:  # 6 元組:單一圖檔,預設 圖_<年>.pdf
            page, num, x0, y0, x1, y1 = entry
            pdf = f'圖_{year}.pdf'
        grouped.setdefault(num, []).append((pdf, page, x0, y0, x1, y1))

    paths = {}  # num -> 相對於 public 的路徑
    for num, regions in grouped.items():
        imgs = [render(*r) for r in regions]
        if len(imgs) == 1:
            out = imgs[0]
        else:
            w = max(im.width for im in imgs)
            gap = 12
            h = sum(im.height for im in imgs) + gap * (len(imgs) - 1)
            out = Image.new('RGB', (w, h), 'white')
            y = 0
            for im in imgs:
                out.paste(im, (0, y))
                y += im.height + gap
        fn = out_dir / f'{num}.png'
        out.save(fn)
        paths[num] = f'images/{year}/{num}.png'
        print(f'  Q{num}: {fn} ({out.width}x{out.height}, {len(imgs)} 區塊)')

    # 回填 JSON
    qs = json.loads(json_path.read_text(encoding='utf-8'))
    patched = 0
    for q in qs:
        if q['num'] in paths:
            q['image'] = paths[q['num']]
            q['needsImage'] = True
            patched += 1
    json_path.write_text(json.dumps(qs, ensure_ascii=False, indent=2), encoding='utf-8')
    print(f'已裁切 {len(paths)} 張、回填 {patched} 題 → {json_path}')


if __name__ == '__main__':
    main()
